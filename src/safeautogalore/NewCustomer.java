    private void regBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regBtnActionPerformed
        // TODO add your handling code here:
        if(indRadBtn.isSelected()){
            if(nameField.getText().isEmpty() || addressField.getText().isEmpty() ||
                    phoneField.getText().isEmpty() || emailField.getText().isEmpty() || 
                    licenseOrRegField.getText().isEmpty() || String.valueOf(passField1.getPassword()).isEmpty() ||
                    String.valueOf(passField2.getPassword()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Please enter all fields");
            } else if(String.valueOf(passField1.getPassword()).equals(String.valueOf(passField2.getPassword()))){
                String name = nameField.getText();
                String address = addressField.getText();
                String phoneNo = phoneField.getText().trim();
                String email = emailField.getText().trim();
                String licenseNo = licenseOrRegField.getText().trim();
                String password = String.valueOf(passField1.getPassword());
                                
                Individual individual = new Individual(name,address,phoneNo, email,licenseNo);
                individual.setPassword(password);
                individuals.add(individual);
                saveIndividualToFile();
                this.dispose();
            
            } else {
                JOptionPane.showMessageDialog(null, "Password mismatch");
                passField1.setText("");
                passField2.setText("");
            }
        } else {
            if(nameField.getText().isEmpty() || addressField.getText().isEmpty() ||
                    phoneField.getText().isEmpty() || emailField.getText().isEmpty() || 
                    licenseOrRegField.getText().isEmpty()  || String.valueOf(passField1.getPassword()).isEmpty() ||
                    String.valueOf(passField2.getPassword()).isEmpty()){
                JOptionPane.showMessageDialog(null, "Please enter all fields");
            } else if(String.valueOf(passField1.getPassword()).equals(String.valueOf(passField2.getPassword()))){
                String name = nameField.getText();
                String address = addressField.getText();
                String phoneNo = phoneField.getText().trim();
                String email = emailField.getText().trim();
                String regNo = licenseOrRegField.getText().trim();
                String password = String.valueOf(passField1.getPassword());
                
                
                Company company = new Company(name,address,phoneNo, email,regNo);
                company.setIsIndividual(false);
                company.setPassword(password);
                
                companies.add(company);
                
                saveCompanyToFile();
                this.dispose();
                
            } else {
                JOptionPane.showMessageDialog(null, "Password Mismatch");
                passField1.setText("");
                passField2.setText("");
            }
        }
    }//GEN-LAST:event_regBtnActionPerformed

