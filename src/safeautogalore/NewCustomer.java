    public void populateArrayLists(){
        try{
            FileInputStream file = new FileInputStream("individuals.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            
            boolean endOfFile = false;
            
            while(!endOfFile){
                try{
                    individuals.add((Individual)inputFile.readObject());
                } catch(EOFException f){
                    endOfFile = true;
                } catch(ClassNotFoundException cl){
                    JOptionPane.showMessageDialog(null, "class not found");
                }
            }
            
            
            file.close();
            
            FileInputStream file2 = new FileInputStream("companies.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);
            
            boolean endOfFile2 = false;
            
            while(!endOfFile2){
                try{
                    companies.add((Company)inputFile2.readObject());
                } catch(EOFException f){
                    endOfFile2 = true;
                } catch(ClassNotFoundException cl){
                    JOptionPane.showMessageDialog(null, "Class not found");
                }
            }
            
            file2.close();
            
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    // Save all customers to file
    public void saveIndividualToFile(){
        try{
            FileOutputStream file = new FileOutputStream("individuals.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(Individual individual: individuals){
                outputFile.writeObject(individual);
            }
            outputFile.close();
           
            JOptionPane.showMessageDialog(null, "Individual Successfully Registered!");
            
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void saveCompanyToFile(){
        try {
            FileOutputStream file2 = new FileOutputStream("companies.dat");
            ObjectOutputStream outputFile2 = new ObjectOutputStream(file2);

            for(Company company: companies){
                outputFile2.writeObject(company);
            }
            
            JOptionPane.showMessageDialog(null, "Company Successfully Registered!");
           
        } catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

